    public ArrayList<Producto> getProductos() 
    {
        String sql = "select * from producto";
        ArrayList<Producto> lstProducto = new ArrayList<>();
        try{
            connection = con.gertConnection();
            st = connection.createStatement();
            rs = st.executeQuery(sql);
            
            while(rs.next()){
                Producto producto = new Producto();
                producto.setCantidadProducto(rs.getInt("cantidadProducto"));
                producto.setCodigoProducto(rs.getString("codigoProducto"));
                producto.setDescripcion(rs.getString("descripcion"));
                producto.setNombreProducto(rs.getString("nombreProducto"));
                producto.setPrecioUnitario(rs.getFloat("precioUnitario"));
                lstProducto.add(producto);
                
            }
        }catch(Exception e){
            System.out.println(e);
        }
        
        return lstProducto;
    }
     
    private void FillCbx(){
        cbxProducto.removeAllItems();
        
        for (int i = 0; i < lstproductos.size(); i++) {
            cbxProducto.addItem(new Producto(lstproductos.get(i).getCodigoProducto(),lstproductos.get(i).getNombreProducto()
                    ,lstproductos.get(i).getPrecioUnitario(),lstproductos.get(i).getCantidadProducto(),
                    lstproductos.get(i).getDescripcion()));
        }
    }
    
    private void Price(){
        unitPrice = cbxProducto.getItemAt(cbxProducto.getSelectedIndex()).getPrecioUnitario();
        amount = Integer.parseInt(spnCantidad.getValue().toString());
        max = cbxProducto.getItemAt(cbxProducto.getSelectedIndex()).getCantidadProducto();
        lblPrecio.setText(Pay(unitPrice));
        lblTotal.setText(Pay(unitPrice*amount));
        SpinnerModel model = spnCantidad.getModel();
        ((SpinnerNumberModel) model).setMaximum(max);
        
    }
    
    private String Pay(float price){
        return "Q " + Math.round(price*100.0)/100.0;
    }
    
    private void UpdateTable(){
        
        while(tblProducto.getRowCount() > 0 ){
            tblProducto.removeRow(0);
        }
         total = 0;
        for(Venta v: lstVenta){
        
            Object venta[] = new Object[4];
            venta[0] = v.getCodigoProducto();
            venta[1] = v.getNombre();
            venta[2] = v.getCantidad();
            venta[3] = v.getTotal();  
            total = (float) (total + (v.getCantidad() *v.getPrecioU()));
            tblProducto.addRow(venta);
        }
        
        tblVentas.setModel(tblProducto);
        lblTotalCompra.setText(Pay(total));
    }
    
    private void clean(){
        unitPrice = 0;
        amount = 1;
        cbxProducto.setSelectedIndex(0);
        spnCantidad.setValue(1);
        Price();
        
    }
    
    private boolean findSale(Venta newSale){
        for (Venta v: lstVenta){
            if(v.getCodigoProducto() == newSale.getCodigoProducto()){
                int newAmount = v.getCantidad() + newSale.getCantidad();
                v.setCantidad(newAmount);
                v.setTotal(Pay(unitPrice *newAmount));
                return true;
            }
        }
        return false;
    }
    
