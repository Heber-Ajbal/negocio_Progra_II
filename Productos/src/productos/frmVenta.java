    private int CreateBilling(){
        
        int generatedId = 0; 
        try {
    Date fechaActual = new Date();
    String sql = "insert into factura (idPedido, fecha, total) values (?, ?,?)";
    
    connection = con.gertConnection();
    PreparedStatement preparedStatement = connection.prepareStatement(sql, Statement.RETURN_GENERATED_KEYS);
    preparedStatement.setString(1,NumberBilling());	
    preparedStatement.setDate(2, new java.sql.Date(fechaActual.getTime()));
    preparedStatement.setDouble(3, total);
    
    int rowsAffected = preparedStatement.executeUpdate();
    
    if (rowsAffected > 0) {
        ResultSet generatedKeys = preparedStatement.getGeneratedKeys();
        if (generatedKeys.next()) {
            generatedId = generatedKeys.getInt(1);
            JOptionPane.showMessageDialog(null, "Se ha guardado correctamente la factura");
        }
    } else {
        JOptionPane.showMessageDialog(null, "No se pudo guardar la factura.");
    }
    
    } catch (Exception ex) {
        JOptionPane.showMessageDialog(null, ex);
    }
        return generatedId;
    }
    
